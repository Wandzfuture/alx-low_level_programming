#include <stdio.h>

/**
 * add -  Adds two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: sum of a and b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: the difference of a and b
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul -  multiples two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: Quadrant of a and b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div -  divides the two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: quotient of a and b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod -  returns remainder of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: the remainder of a and b
 */
int mod(int a, int b)
{
	return (a % b);
}
